public class Ejem001 {
    public static void main(String[] args){
        /* 
            Variables para uso de memoria
            Declarar por tipo nombreVariable
            Para nombreVariable:
            0. Usar variables con nombres que
            describan aquello que van a almacenar
            1. Usar solamente letras mayúsculas
            minúsculas, números y guiones bajo, siempre
			iniciando con una letra
            2. Generalmente se usa la notación
            camello, la cual indica que si la
            variable requiere de varias palabras
            la segunda inicial comienza con 
            mayúscula:  unaVariableNueva
        */
        // A las siguientes se les conoce como
        // Tipos de datos primitivos
        // boolean (1 bit)
        boolean buleano = true;
        // caracter (8 bits)
        char carac = 'a';
		// byte (8 bits)
		byte bait = 3;
        // entero corto (16 bits)
        short enteroCorto = 0;
        // entero (32 bits)
        int entero = 69;
        // entero largo (64 bits) [solamente para uP de 64 bits]
        long enteroLargo = 372193;
        // flotante (32 bits) [Usar f al final]
        float flotante = 2.7172f;
        // doble (64 bits)
        double doble = 3.14159265358979;
    }
}
